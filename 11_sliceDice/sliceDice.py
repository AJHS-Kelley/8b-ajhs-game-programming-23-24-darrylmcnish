alphabet = "abcdefghijklmnopqurstuvwxyz"
myString = "Type a random string with punctuation, please.\n"

print(alphabet[0])
print(alphabet[-1])

# slicing
print(alphabet[0:4]) #SLICE UP TO SECOND INDEX.
# alphabet[start:stop]

# Slice from the start
print(alphabet[:27])

# Slice to the End
print(alphabet[12:])

# Slice the whole %#$#! Thing
print(alphabet[:])

# Negative Index Slicing
print(alphabet[-4:-1])